;R0 = Register that contains either 0 or 1 to display
;R1 = Register that contains contents to be Printed
;R2 = counter for multiplicatoin
;R3 = Address of line to be printed
;R4 = 8-bit COLUMN counter
;R5 = ROW counter (16~0)

			.ORIG x3000		;Starting address is x3000
PRINT0		.FILL x5000		;
PRINT1		.FILL x5001
TARGET		.FILL x5002
			AND R0, R0, #0	; Clears register R0

			AND R2, R2, #0	; Clears register R2
			ADD R2, R2, #4	; Stores decimal 4 in R2

			AND R3, R3, #0	; Clears register R3

			AND R4, R4, #0	; Clears register R4
			ADD R4, R4, #8	; Stores decimal 8 in R4

			AND R5, R5, #0	; Clears register R5
			ADD R5, R5, #8	; Stores decimal 8 in R5
			ADD R5, R5, #8	; Stores decimal 16 in R5

			AND R1, R1, #0	; Clears register R1
			LDI R1, TARGET	; Loads the target ASCII character in R1

COUNTER		ADD R1, R1, R1	; Add R1 and store it self in R1
			ADD R2, R2, #-1	; Reduce the counter R2 by 1
			BRp COUNTER		; Keep adding, until it gets to M(R1)*16
			LEA R0, FONT_DATA ;Store address in FONT_DATA into R0
			ADD R3, R0, R1	; Store the final address for the target ASCII character in R3
			LDR R1, R3, #0	;store the value in R3 to R1, set condition code

OUTPUT		ADD R4, R4, #0	;Add 0 to R4 to set condition code
			BRz ROW_DONE	;If R4=0, indicates end of the ROW and go to label ROW_DONE

			ADD R1, R1, #0 	;Add 0 to R1 to set condition code
			BRn OUTPUT1		;if R1<0, PC+5
			LDI R0, PRINT0	;store the value in address x5000 in R0
			ADD R4, R4, #-1 ;Count down the 8-bit column counter
			ADD R1, R1, R1	;bitwise left-shift to determine next digit
			OUT

			BRnzp OUTPUT 	; go back to label OUTPUT
OUTPUT1		LDI R0, PRINT1	;store the value in address x5001 in R0
			ADD R4, R4, #-1	;Count down the 8-bit column counter
			ADD R1, R1, R1	;bitwise left-shift to determine next digit
			OUT

			BRnzp OUTPUT	; go back to label OUTPUT

ROW_DONE	LD R0, ASCII_NL	;Load contents in label ASCII_NL in R0
			OUT
			BRnzp NEXT_ROW

ASCII_NL	.FILL x0A		;end each line printed to monitor
			BRnzp NEXT_ROW

NEXT_ROW	ADD R5, R5, #0	;Add 0 to R5 to set conditioncode
			BRz DONE		;IF R5=0, go to label DONE

			ADD R3, R3, #1	;Add 1 to the address so that the program prints next row
			ADD R5, R5, #-1	; Reduce ROW counter by 1
			ADD R4, R4, #8	;reset the 8-bit COLUMN counter
			BRnzp OUTPUT	;go back to label OUTPUT

DONE		HALT



