;;Write a program in LC-3 assembly code to print a null-
;terminated string stored from memory location x5002
;in a large (8x16-pixel) font to the monitor using two
;other ASCII characters stored at x5000 to represent
;bit 0, and at x5001 to represent bit 1.
;
;Core logic is similar to Lab 13: In orde to check
;which bit in FONT_DATA has bit 0 or bit 1, I simply
;AND with x8000,x4000,x2000...If the result of the AND
;has a negative or postive value, it means there is a
;bit 1; otherwise, there is a bit 0.
;
;I add another iterative construct within each row
;before the column iterative construct to print
;the single line of each characters.

; R0 The content at each FONT_DATA
; R1 Row counter (Start from 0 to 16)
; R2 The address of the each String’s character
; R3 The Content in the each String’s character
; R4 Column counter (Starting from 8 to 0)
; R5 Values like x0800 to determine 0 or 1 in FONT_DATA
; R6 A counter loading addresses of values like x0800

.ORIG x3000

  AND R1, R1, #0      ;Initialize the row counter

NEXT_ROW
  AND R4, R1, #-1
  ADD R4, R4, #-16    ;If R1, the row counter, reaches 16,
  BRz DONE            ;the program is finished.
  LD R2, CHA          ;The starting address of the string
  ADD R2, R2, #-1

NEXT_CHA
  ADD R2, R2, #1
  LEA R6, FIR
  LDR R3, R2, #0      ;Load the content at string address to R3
  BRz DONE_ROW        ;If the loaded character is 0, the the row is done

  AND R4, R4, #0      ;Set up a temporary counter for multiplication.
  ADD R4, R4, #4      ;If we add the number itself for 4 times,
FIND
  ADD R3, R3, R3      ;it is the same to multiply by 16.
  ADD R4, R4, #-1     ;E.g 1+1=2, 2+2=4, 4+4=8, 8+8=16 (1*16=16).
  BRp FIND            ;If R3 is postive, we keep adding the number.
  LEA R4, FONT_DATA
  ADD R3, R3, R4
  ADD R3, R3, R1      ;Determines the new ROW by adding the row number

  ; column counter set to 8
  AND R4, R4, #0
  ADD R4, R4, #8

NEXT_COLUMN
  ADD R4, R4, #0
  BRz NEXT_CHA

  ; print the next char
  LDR R5, R6, #0      ;Load the value of the next value to be ANDed.
  ADD R6, R6, #1      ;The counter for the next address increments.
  LDR R0, R3, #0      ;Load the value of FONT_DATA to R0 temporarily.
  AND R5, R5, R0      ;AND the FONT_DATA value and values like x4000.
  BRnp YES
  LDI R0, ZER         ;If there is a bit 0,
  OUT                 ;print bit 0 value
  ADD R4, R4, #-1
  BRnzp NEXT_COLUMN   ;Go to the next column,
YES
  LDI R0, ONE         ;If there is a bit 1(either negative or positive),
  OUT                 ;print bit 1 value,
  ADD R4, R4, #-1
  BRnzp NEXT_COLUMN   ;Go to the next column,

DONE_ROW
  ;print new line char
  LD R0, NLI          ;Load the NEXTLINE ASCII to R0 to print.
  OUT
  ADD R1, R1, #1      ;Memory location of FONT_DATA increments.
  BRnzp NEXT_ROW      ;Go to the next row.

DONE
  HALT
;
ZER .FILL x5000       ;memory location of bit 0 value
ONE .FILL x5001       ;memory location of bit 1 value
CHA .FILL x5002       ;memory location of the rendered character
NLI .FILL x000A       ;ASCII value of a new line

;a list of values to be ANDed
FIR .FILL x8000
    .FILL x4000
    .FILL x2000
    .FILL x1000
    .FILL x0800
    .FILL x0400
    .FILL x0200
    .FILL x0100


