.ORIG x3000

START_GETC
  LDI R1, KBSR
  BRzp START_GETC
  LDI R0, KBDR

START_OUT
  LDI R1, DSR
  BRzp START_OUT
  STI R0, DDR


KBSR    .FILL xFE00
KBDR    .FILL xFE02
DSR     .FILL xFE04
DDR     .FILL xFE06
INV_MSG .STRINGZ "Invalid Phone Number."
VAL_MSG .STRINGZ "Valid Phone Number."
ENTER .FILL x000D
NUM0 .FILL x0030
NUM9 .FILL x0039

.END
